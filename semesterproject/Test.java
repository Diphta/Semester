    public void drawSeats(Graphics g) {
        Hall hal = new Hall("Sal 1", 14, 20);
        hal.fillSeatsSal1();
        for (Seat[] chair: hal.seats) {
            for (Seat seat: chair) {
                System.out.println(seat.toString());
                seat.draw(g);
            }
        }
    }
